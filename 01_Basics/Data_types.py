# data types

# Number
x = 10
y = 12

z = 2.5

sum = x + y
product = z*5

print("product: " , product)
print("sum: ", sum)

# String 

username = "demouser"
print("username: ", username)

print("username first character: ", username[0])
print("username last character: ", username[-1])
print("username 1 to 5 character: ", username[1:5])

print("length of username: ", len(username))

# List - like array

mylist = [123, "code", 3.14]
print("mylist: ", mylist)

# - Dictionary- like object 

mydict = {'one': "marvel", 'two': 'hello', 'three': "code"}

print(mydict["one"])
